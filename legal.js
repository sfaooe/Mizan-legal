import Anthropic from "@anthropic-ai/sdk";

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });

const SYSTEM_PROMPT = `أنت مساعد قانوني عراقي متخصص وخبير. اسمك "ميزان".

تخصصاتك الكاملة:
- القانون المدني العراقي رقم 40 لسنة 1951 وتعديلاته
- قانون العقوبات العراقي رقم 111 لسنة 1969
- قانون أصول المحاكمات المدنية والجزائية
- قانون الأحوال الشخصية رقم 188 لسنة 1959
- قانون الشركات والتجارة والعمل العراقي
- الدستور العراقي لعام 2005
- قرارات محكمة التمييز الاتحادية والسوابق القضائية
- التشريعات والأنظمة الوزارية العراقية

مهامك:
1. تحليل القضايا القانونية وتقديم رأي قانوني مفصل مع الاستشهاد بالمواد
2. صياغة الدعاوى القضائية بشكل احترافي وفق أصول المحاكمات
3. تحليل العقود وكشف الثغرات القانونية
4. اقتراح الدفوع القانونية المناسبة مع الأسانيد
5. البحث في القوانين والأنظمة العراقية
6. تقدير احتمالية نجاح القضية مع التبرير

قواعد الإجابة:
- استشهد دائماً بأرقام المواد والقوانين بدقة
- قدّم تحليلاً منطقياً ومنظماً بعناوين واضحة
- أشر إلى نقاط القوة والضعف في القضية
- استخدم المصطلحات القانونية العراقية الصحيحة
- كن واضحاً وعملياً في اقتراحاتك`;

const MODE_PROMPTS = {
  analyze: (t) => `قم بتحليل قانوني شامل للقضية التالية وفق القانون العراقي. حدد: 1) الإطار القانوني المنطبق مع المواد 2) نقاط القوة والضعف 3) الإجراءات القانونية المقترحة 4) احتمالية النجاح مع التبرير:\n\n${t}`,
  draft: (t) => `صِغ دعوى قضائية احترافية كاملة وفق أصول المحاكمات العراقية تتضمن: المحكمة المختصة، أطراف الدعوى، الوقائع، الأسانيد القانونية، الطلبات:\n\n${t}`,
  contract: (t) => `حلّل هذا العقد قانونياً من حيث: 1) صحة البنود وفق القانون المدني العراقي 2) الثغرات والمخاطر 3) البنود الإشكالية 4) التوصيات:\n\n${t}`,
  search: (t) => `قدّم بحثاً قانونياً مفصلاً مع الاستشهاد بالمواد والقوانين العراقية ذات الصلة:\n\n${t}`,
  defense: (t) => `اقترح أفضل الدفوع القانونية الممكنة مستنداً إلى القوانين والمواد العراقية المحددة:\n\n${t}`,
};

export default async function handler(req, res) {
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" });

  const { message, mode } = req.body;
  if (!message || !mode) return res.status(400).json({ error: "Missing message or mode" });

  const prompt = MODE_PROMPTS[mode] ? MODE_PROMPTS[mode](message) : message;

  try {
    const response = await client.messages.create({
      model: "claude-sonnet-4-20250514",
      max_tokens: 2048,
      system: SYSTEM_PROMPT,
      messages: [{ role: "user", content: prompt }],
    });

    const text = response.content.map((b) => b.text || "").join("");
    res.status(200).json({ result: text });
  } catch (err) {
    console.error(err);
    res.status(500).json({ error: err.message || "Internal server error" });
  }
}
